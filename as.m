t = 0:0.0001:0.02;
x_c = sin(2*pi*60*t); 
fs1 = 150;
fs2 = 1600; 
Ts1 = 1/fs1; 
Ts2 = 1/fs2; 
n1 = 0:Ts1:0.02;
n2 = 0:Ts2:0.02;
x_s1 = sin(2*pi*60*n1);
x_s2 = sin(2*pi*60*n2);
figure;
subplot(3,1,1);
plot(t, x_c, 'Color', 'blue');
xlabel('Time (s)');
ylabel('x(t)');
grid on;
text(0.016, -0.5, 'Continuous', 'FontSize', 12);
subplot(3,1,2);
stem(n1, x_s1, 'bo-', 'LineWidth', 1.5, 'Color', 'green');
xlabel('Time (s)');
ylabel('x_{150}[n]');
grid on;
text(0.01, 0.8, '2.5 samples/period', 'FontSize', 12);
subplot(3,1,3);
stem(n2, x_s2, 'bo-', 'LineWidth', 1.5, 'Color','r');
hold on;
hold off;
text(0.01, 0.8, '26.67 samples/period', 'FontSize', 12);
xlabel('Sample Index (n)');
ylabel('x_{1600}[n]');
grid on;